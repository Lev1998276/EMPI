		
CREATE TABLE SUD.EMPI_ERROR_RECORDS 
(
	ERROR_FILE_ID INT AUTO_INCREMENT PRIMARY KEY,
	ERROR_FILENAME VARCHAR(255),
	ERROR_DESCRIPTION VARCHAR(255) DEFAULT 'CHECK LOG FILE FOR DESCRIPTION',
	ERROR_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);



		
CREATE TABLE SUD.EMPI_POSTED_RECORDS 
(
	POSTED_FILE_ID INT AUTO_INCREMENT PRIMARY KEY,
	POSTED_FILENAME VARCHAR(255),
	POSTED_DESCRIPTION VARCHAR(255) DEFAULT 'FILE WAS SUCCESSFULLY POSTED TO API',
	POSTED_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP
)


CREATE TABLE SUD.EMPI_API_CREDENTIALS (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    API_USERNAME VARCHAR(255) NOT NULL,
    API_PASSWORD VARCHAR(255) NOT NULL,
    UPDATE_TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
